import java.util.*;

public class Arrays_basic {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int marks[] = new int[5];   //Creation of array

        int numbers[] =  {1, 2, 3};  //Creation of array
        
        int moreNumbera[] = {4, 5, 6};

        String fruits[] = {"apple", "mango", "orange"};  //Creation of array

        System.out.println(numbers[0]);
        System.out.println(fruits[2]);
        System.out.println(moreNumbera[1]);
        System.out.println(marks[0]);

        sc.close();
    }
} 